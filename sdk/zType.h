#ifndef Z_TYPE_H
#define Z_TYPE_H

/**
 * \brief 4字节无符号数
 *
 */
typedef unsigned int DWORD;
/**
 * \brief 8字节无符号数
 *
 */
typedef unsigned long long QWORD;
/**
 * \brief 1字节Byte
 *
 */
typedef unsigned char BYTE;
/**
 * \brief 4字节有符号数
 *
 */
typedef int SDWORD;
/**
 * \brief 8字节有符号数
 * 
 */ 
typedef long long SQWORD;
/**
 * \brief 单精度浮点数
 * 
 */ 
typedef float FLOAT;
/**
 * \brief 双精度浮点数
 * 
 */ 
typedef double DFLOAT;
/**
 * \brief 2字节无符号数
 * 
 */
typedef unsigned short WORD;
/**
 * \brief 2字节有符号数
 * 
 */
typedef short SWORD;  

#endif